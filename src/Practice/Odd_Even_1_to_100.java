package Practice;

public class Odd_Even_1_to_100 {

	 public static void main(String[]args){

		  

		  for(int i=0;i<=100;i++){


		    if(i%2==0){


		          System.out.println(i + " Even");

		       }

		       else{

		           System.out.println(i + " Odd");

		          }

		  }

	 }
}


	
	
	
	
	
	

